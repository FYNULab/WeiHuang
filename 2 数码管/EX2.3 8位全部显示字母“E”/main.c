
/***************************************************************************************************    
工程名称：	8smg
功能描述：	控制数码管8位全部显示"E"
硬件连接：  用8位杜邦线将J8与J12连接，2位杜邦线分别将J11_0与J15_DS1、J11_1与J15_DS2连接。
维护记录：  2011-8-22
***************************************************************************************************/
#include "reg51.h"       //包含头文件

sbit LE1=P2^0;           //位选573锁存器使能
sbit LE2=P2^1;           //段选573锁存器使能


#define uchar unsigned char
#define uint  unsigned int

//**************************************************************************************************
//主函数
//**************************************************************************************************
void main()               //主函数
{ 
 while(1)                 //进入while死循环
  {
     P1=0x00;             //0x00=0000 0000,即选通数码管的全部8位
     LE1=1;               //锁存位
     LE1=0;               //断开锁存,位选573的Q7~Q0仍为0x00

     P1=0x79;             //0x79为"E"的编码
     LE2=1;               //锁存段码
     LE2=0;               //断开锁存,段选573的Q7~Q0仍为0x79
   }         
}